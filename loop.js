let sum = 0;

// for
for (let i = 0; i <= 5; i++) {
    sum = sum + i;
}
console.log("the sum of 1st n numbers is", sum);

// while
let j = 0;
let Sum = 0;
while (j <= 5) {
    Sum = Sum + j;
    j++;

}
console.log("the sum of 1st n numbers is", Sum);

// do-while
answer = 0;
let k = 0;
do {
    answer = answer + k;
    k++;

} while (k <= 5);
